package student

import (
	"testing"
)

func TestAtoi(t *testing.T) {
	tables := []struct {
		a string
		i int
	}{
		{"99999999999999999999", 0},
		{"+9999999999999999999", 0},
		{"+999999999999999999", 999999999999999999},
		{"-999999999999999999", -999999999999999999},
		{"0000000012345", 12345},
		{"012 345", 0},
		{"Hello World!", 0},
		{"+1234", 1234},
		{"-1234", -1234},
		{"++1234", 0},
		{"", 0},
		{"9223372036854775808", 0},
		{"9223372036854775807", 9223372036854775807},
		{"-9223372036854775808", -9223372036854775808},
		{"-9223372036854775809", 0},
		{"12345", 12345},
		{"0000000012345", 12345},
		{"012 345", 0},
		{"Hello World!", 0},
		{"+1234", 1234},
		{"-1234", -1234},
		{"++1234", 0},
		{"--1234", 0},
		{"-", 0},
		{"1", 1},
		{"-3", -3},
		{"8292", 8292},
	}
	for _, table := range tables {
		total := Atoi(table.a)
		if total != table.i {
			t.Errorf("Atoi of (%s) was incorrect, got: %d, want: %d.", table.a, total, table.i)
		}
	}
}
